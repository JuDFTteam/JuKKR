subroutine energy_loop


do IE=1,IEMAX

  -calculate t-matrix of atoms

  -calculate GF by Dyson eq. or similar

  -calculate R_ll H_ll

  -calculate charge density to integrate over energies

  -add up charge density to integrate over energies

end do !IE=1,IEMAX

-output: charge density

-calculate total energies

-calculate output potential

-mix input/output potential -> new input potential


end subroutine energy_loop